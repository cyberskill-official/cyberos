# INVARIANT: every PATH_RENAMES entry needs a content rule for its basename,
# or callers keep invoking a filename that no longer exists. Checked by --verify.
def unreferenced_renames() -> list[str]:
    stems = {Path(src).stem.replace(".", "") for src, _ in PATH_RENAMES}
    covered = " ".join(pat for _n, pat, _r in RULES)
    return sorted(s for s in stems
                  if ("fr" in s.lower().split("_") or "-fr-" in s or "_fr_" in s)
                  and s not in covered)

# ─────────────────────────────────────────────────────────────────────────────
# IDEMPOTENCY GUARD.
#
# Three rules are ONE-SHOT. They free the word `task` by demoting the old
# task@1 contract to subtask@1. Once pass 1 has run, `task@1` means the NEW
# thing (the former feature_request@1). Re-running them would demote THAT to
# subtask@1 and collapse both contracts into one — silently, across 216 sites.
#
# Every other rule is idempotent: its left-hand side no longer exists after the
# first run, so a second pass is a no-op.
#
# The guard keys off the on-disk marker that pass 1 leaves behind.
# ─────────────────────────────────────────────────────────────────────────────
ONE_SHOT_RULES = frozenset({
    "free-word:contract-literal",   # task@1 -> subtask@1
    "free-word:contract-id",        # contract_id: task -> subtask
    "free-word:contract-path",      # contracts/task -> contracts/subtask
})

# The PATH rename is one-shot for the same reason, and guarding only the content
# rules is not enough. Learned the hard way on the pass-2 run: `contracts/task ->
# contracts/subtask` re-fired and swallowed the NEW task contract into
# contracts/subtask/task/, taking STATUS-REFERENCE.md with it and breaking
# build.sh. An "is the source absent?" check does NOT catch this — the source very
# much exists, it is just a different thing now.
ONE_SHOT_PATH_RENAMES = frozenset({
    ("modules/skill/contracts/task", "modules/skill/contracts/subtask"),
})


def word_already_freed(root: Path) -> bool:
    """True once pass 1 has demoted the old task@1 contract to subtask@1."""
    return (root / "modules" / "skill" / "contracts" / "subtask").is_dir()


COMPILED_ALL = [(name, re.compile(pat, re.MULTILINE), rep) for name, pat, rep in RULES]
COMPILED = COMPILED_ALL   # rebound in main() once the repo root is known


def repo_root() -> Path:
    out = subprocess.run(
        ["git", "rev-parse", "--show-toplevel"],
        capture_output=True, text=True, check=True,
    )
    return Path(out.stdout.strip())


def tracked_files(root: Path) -> list[str]:
    out = subprocess.run(
        ["git", "ls-files", "-z"], cwd=root, capture_output=True, text=True, check=True,
    )
    return [p for p in out.stdout.split("\0") if p]


# ─────────────────────────────────────────────────────────────────────────────
# Tracked SYMLINKS. git ls-files lists them like ordinary paths, so a naive
# read_text() either explodes (IsADirectoryError, when the link points at a
# directory) or — far worse — writes THROUGH the link into a target that is
# itself tracked and already rewritten.
#
# This repo has six:
#   AGENTS.md                                 -> modules/memory/cyberos/data/AGENTS.md
#   CLAUDE.md                                 -> modules/memory/cyberos/data/AGENTS.md
#   .codex/skills/ship-feature-requests       -> ../../.cyberos/plugin/skills/ship-feature-requests
#   .commandcode/skills/ship-feature-requests -> (same)
#   .grok/skills/ship-feature-requests        -> (same)
#   .opencode/skill/ship-feature-requests     -> (same)
#
# AGENTS.md and CLAUDE.md point at the SAME file. Planning three edits that all
# write to one target is how you corrupt the memory protocol: the third read
# would see the already-rewritten body and fire the one-shot `task@1 ->
# subtask@1` rule on it. The two-phase apply (plan everything, then write)
# happens to prevent this, but do not rely on that — skip links outright. Their
# targets are tracked separately and get rewritten on their own.
# ─────────────────────────────────────────────────────────────────────────────
SYMLINK_RENAMES = (
    (".codex/skills",       "ship-feature-requests", "ship-tasks"),
    (".commandcode/skills", "ship-feature-requests", "ship-tasks"),
    (".grok/skills",        "ship-feature-requests", "ship-tasks"),
    (".opencode/skill",     "ship-feature-requests", "ship-tasks"),
    (".claude/skills",      "ship-feature-requests", "ship-tasks"),   # gitignored, fix anyway
)


def in_scope(rel: str) -> bool:
    if any(rel.startswith(p) for p in DENY_PREFIXES):
        return False
    if rel in SEALED_PATHS:
        return False
    if any(rel.endswith(s) for s in SKIP_SUFFIXES):
        return False
    return True


def fix_symlinks(root: Path) -> None:
    """Retarget the agent-tool skill symlinks: ship-feature-requests -> ship-tasks.

    These are how Claude Code, Codex, Grok, opencode and commandcode discover the
    workflow. Four of the five are TRACKED. Leave them and every agent keeps
    resolving a skill name that no longer exists the moment build.sh regenerates
    the payload.
    """
    print("\n--- agent skill symlinks ---")
    for d, old, new in SYMLINK_RENAMES:
        old_p = root / d / old
        new_p = root / d / new
        if not old_p.is_symlink():
            print(f"skip (absent)  {d}/{old}")
            continue
        target = __import__("os").readlink(old_p)
        new_target = target.replace(old, new)
        tracked = subprocess.run(
            ["git", "ls-files", "--error-unmatch", f"{d}/{old}"],
            cwd=root, capture_output=True, text=True,
        ).returncode == 0
        if new_p.is_symlink() or new_p.exists():
            print(f"skip (done)    {d}/{new} already exists")
            continue
        if tracked:
            subprocess.run(["git", "rm", "-q", "--cached", f"{d}/{old}"], cwd=root, check=True)
        old_p.unlink()
        new_p.symlink_to(new_target)
        if tracked:
            # -f is REQUIRED: these agent dirs are gitignored, and the old links
            # were force-added. Without -f, git refuses and the run dies here,
            # halfway through the symlink set.
            subprocess.run(["git", "add", "-f", f"{d}/{new}"], cwd=root, check=True)
        print(f"retargeted     {d}/{new} -> {new_target}"
              f"{'  (tracked)' if tracked else '  (gitignored)'}")
    print("\n  Also update the managed block in .gitignore (it names the OLD paths),")
    print("  and rebuild the payload so the symlink targets actually exist:")
    print("      bash tools/cyberos-init/build.sh")


def report_sealed(root: Path) -> None:
    """Show, but never write, the edits the codemod would make to sealed tests."""
    print("\n--- SEALED held-out tests (codemod refuses to write; do these by hand) ---")
    for rel in SEALED_PATHS:
        p = root / rel
        if not p.exists():
            print(f"  absent: {rel}")
            continue
        src = p.read_text(encoding="utf-8")
        new, hits = rewrite(src, rel)
        if not hits:
            print(f"  clean:  {rel}")
            continue
        print(f"\n  {rel}   ({sum(hits.values())} edits)")
        for i, (a, b) in enumerate(zip(src.splitlines(), new.splitlines()), 1):
            if a != b:
                kind = "DATA" if not a.lstrip().startswith(('"""', "#", "'''")) else "docstring"
                print(f"    L{i} [{kind}]")
                print(f"      - {a.strip()[:96]}")
                print(f"      + {b.strip()[:96]}")
    print("""
  To apply, as a deliberate human act:
      chmod u+w modules/memory/tests/test_claude_code_hook.py \\
                modules/memory/tests/test_memory_sync.py \\
                modules/memory/tests/test_sync_class.py
      # edit the lines above by hand, then re-seal:
      python -m awh lock modules/memory/tests
      git commit -m "test(memory): unseal held-out tests for fr->task rename, re-sealed"

  Before you touch test_claude_code_hook.py L82, decide the DATA question:
  claude_code_hook.py:90 writes `"source": "fr-memory-109"` into memory-file
  frontmatter. The BRAIN has 0 files carrying it today, so rewriting is free NOW.
  If the hook had ever run, you would have persisted data on one vocabulary and
  new code on another, with the BRAIN deny-listed from this codemod. Either
  rewrite both and note it, or leave `source` as a frozen provenance tag.""")


def _is_comment(line: str) -> bool:
    """A comment / blockquote line — exempt from rewriting (see rewrite())."""
    s = line.lstrip()
    return s.startswith(("#", "//", "*", ">", "<!--", '"""'))


def rewrite(text: str, rel: str = "") -> tuple[str, Counter]:
    """Rewrite one file's text. `rel` gates the code-only rules (see CODE_ONLY_RULES).

    `rel=""` means "no file context" — code-only rules are skipped, because guessing
    wrong on prose is the expensive direction: it corrupts a path or a historical
    filename, and nobody notices until something tries to resolve it.
    """
    original = text
    is_code = rel.endswith(CODE_SUFFIXES)
    hits: Counter = Counter()

    # A COMMENT that explains the retired vocabulary must keep it, or it stops
    # explaining anything. Same exemption the pre-commit hook carries, and the last
    # three files --verify flagged were all this shape:
    #   .githooks/pre-commit  "# ...why it sat there full of stale FR-*.js chunks"
    #   task-author/SKILL.md  "# `FR-` id or a `feature-request` string, so no rule
    #                          matched it"
    #   status-app.js         "// NOTE: the `frs` CSS CLASS below is NOT renamed"
    # Rewriting those turns a true sentence into a false one. Rewrite line by line
    # and leave comment lines alone.
    lines = text.split("\n")
    for i, ln in enumerate(lines):
        if _is_comment(ln):
            continue
        for name, pat, rep in COMPILED:
            if name in CODE_ONLY_RULES and not is_code:
                continue
            ln, n = pat.subn(rep, ln)
            if n:
                hits[name] += n
        lines[i] = ln
    text = "\n".join(lines)
    # Restore frozen provenance literals the rules above may have swept up.
    for wrong, right in FROZEN_LITERALS.items():
        if wrong in text:
            text = text.replace(wrong, right)
            hits["frozen:restored"] += 1

    # A rule that fired and was then fully undone by the frozen-literal restore is
    # not a change. Reporting it as one made `--verify` fail on claude_code_hook.py
    # forever: `id:fr-module-num-lower` rewrote fr-memory-109 -> task-memory-109,
    # FROZEN_LITERALS put it straight back, net text identical — and the hit counter
    # still claimed an edit. A verify that flags a file it does not modify is lying.
    if text == original:
        return text, Counter()
    return text, hits


def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--apply", action="store_true", help="write changes and git mv paths")
    ap.add_argument("--show-files", action="store_true", help="list files touched per rule")
    ap.add_argument("--verify", action="store_true", help="assert zero residue; exit 1 if any")
    ap.add_argument("--residue", action="store_true", help="report sites the rules cannot reach")
    ap.add_argument("--emit-brain-ops", action="store_true",
                    help="emit NDJSON move/put ops for the BRAIN store (do not sed it)")
    ap.add_argument("--sealed", action="store_true",
                    help="show the edits the codemod refuses to make to sealed held-out tests")
    ap.add_argument("--fix-symlinks", action="store_true",
                    help="retarget the agent skill symlinks only (no dirty-tree guard)")
    args = ap.parse_args()

    root = repo_root()

    if args.fix_symlinks:
        fix_symlinks(root)
        return 0

    # ── Idempotency guard: disarm the one-shot rules once pass 1 has run. ───
    global COMPILED
    freed = word_already_freed(root)
    if freed:
        COMPILED = [t for t in COMPILED_ALL if t[0] not in ONE_SHOT_RULES]
        # stderr, not stdout: --emit-brain-ops writes NDJSON to stdout and any
        # human-readable chatter mixed in there corrupts the op stream.
        print("note: contracts/subtask/ exists -> pass 1 already ran.", file=sys.stderr)
        print("      one-shot rules DISARMED (they would demote the new task@1 "
              "to subtask@1):", file=sys.stderr)
        for r in sorted(ONE_SHOT_RULES):
            print(f"        {r}", file=sys.stderr)
        print(file=sys.stderr)

    files = [f for f in tracked_files(root) if in_scope(f)]

    if args.emit_brain_ops:
        return emit_brain_ops(root)

    if args.sealed:
        report_sealed(root)
        return 0

    # ── Guard 1: refuse to apply to a dirty tree. ───────────────────────────
    # `git checkout -- .` must be a valid, complete undo. If the tree already
    # has uncommitted work, a failed apply cannot be cleanly reverted.
    if args.apply:
        dirty = subprocess.run(
            ["git", "status", "--porcelain", "--untracked-files=no"],
            cwd=root, capture_output=True, text=True, check=True,
        ).stdout.strip()
        if dirty:
            print("REFUSING TO APPLY: working tree is dirty.", file=sys.stderr)
            print("Commit or stash first, so `git checkout -- .` is a clean undo.\n",
                  file=sys.stderr)
            print(dirty, file=sys.stderr)
            return 2

    total: Counter = Counter()
    touched: dict[str, list[str]] = {}
    residue: Counter = Counter()
    residue_files: dict[str, set[str]] = {}
    plan: list[tuple[Path, str]] = []   # (path, new_content) — nothing is written yet

    # ── Phase 1: plan every edit in memory. Write nothing. ──────────────────
    skipped_links = 0
    for rel in files:
        path = root / rel
        # Never read or write through a symlink. See SYMLINK_RENAMES above:
        # AGENTS.md and CLAUDE.md both point at modules/memory/cyberos/data/AGENTS.md,
        # and four skill links point at directories. Their targets are tracked
        # separately and get rewritten on their own.
        if path.is_symlink():
            skipped_links += 1
            continue
        try:
            src = path.read_text(encoding="utf-8")
        except (UnicodeDecodeError, FileNotFoundError, IsADirectoryError):
            continue

        new, hits = rewrite(src, rel)

        if args.residue or args.verify:
            # Comment lines are exempt here for the same reason rewrite() skips them:
            # a comment explaining the retired vocabulary has to contain it. Without
            # this, --verify can never reach exit 0 on its own documentation, and a
            # gate that can never pass is one people learn to route around — the exact
            # failure this session kept finding (bash 3.2 `declare -A` silently
            # passing; 29 trigger tests green against a monkeypatched fake).
            probe = "\n".join(
                l for l in (src if args.verify else new).split("\n")
                if not _is_comment(l) and not any(x in l for x in RESIDUE_EXEMPT))
            for rname, rpat in RESIDUE_PATTERNS:
                n = len(rpat.findall(probe))
                if n:
                    residue[rname] += n
                    residue_files.setdefault(rname, set()).add(rel)

        if hits:
            total.update(hits)
            for k in hits:
                touched.setdefault(k, []).append(rel)
            plan.append((path, new))

    changed_files = len(plan)

    # ── Guard 2: pre-flight writability. Abort BEFORE the first write. ──────
    # A crash halfway through leaves a half-renamed repo, which is far worse
    # than not starting. Learned the hard way: an ACL-restricted file killed
    # the first run at file ~1349 of 2498.
    if args.apply:
        unwritable = [p for p, _ in plan if not p.parent.is_dir() or (
            p.exists() and not __import__("os").access(p, __import__("os").W_OK))]
        if unwritable:
            print(f"REFUSING TO APPLY: {len(unwritable)} planned files are not writable.",
                  file=sys.stderr)
            for p in unwritable[:20]:
                print(f"  {p.relative_to(root)}", file=sys.stderr)
            return 2

    # ── Phase 2: commit the plan to disk. ───────────────────────────────────
    if args.apply:
        for path, new in plan:
            path.write_text(new, encoding="utf-8")

    mode = "APPLY" if args.apply else ("VERIFY" if args.verify else "DRY RUN")
    print(f"=== migrate_fr_to_task — {mode} ===")
    print(f"in-scope tracked files : {len(files)}")
    print(f"symlinks skipped       : {skipped_links}  (targets rewritten separately)")
    print(f"files with edits       : {changed_files}")
    print(f"total substitutions    : {sum(total.values())}\n")

    print(f"{'rule':32} {'hits':>7}  files")
    for name, _p, _r in RULES:
        n = total.get(name, 0)
        if n:
            print(f"{name:32} {n:7}  {len(touched.get(name, []))}")
            if args.show_files:
                for f in sorted(touched[name])[:12]:
                    print(f"{'':34}   {f}")
                extra = len(touched[name]) - 12
                if extra > 0:
                    print(f"{'':34}   ... +{extra} more")

    if args.residue or args.verify:
        print(f"\n--- residue (needs a human pass) ---")
        if not residue:
            print("none")
        for rname, n in residue.most_common():
            print(f"{rname:32} {n:7}  {len(residue_files[rname])} files")
            if args.show_files:
                for f in sorted(residue_files[rname])[:10]:
                    print(f"{'':34}   {f}")

        # ── FILENAME residue ────────────────────────────────────────────────────
        # Everything above reads file CONTENTS. Nothing read the NAMES until
        # 2026-07-15, and that whole dimension was invisible to a check reporting
        # "none". What it hid:
        #
        #   306  docs/tasks/_{audits,archive}/**/FR-*.md   each contradicting its own
        #        frontmatter (`task_id: TASK-AI-001` inside `FR-AI-001-*.audit.md`)
        #     1  tools/cyberos-init/templates/FR-TEMPLATE.md — while install.sh's
        #        onboarding block told every new user to `cp ... TASK-TEMPLATE.md`.
        #        The first command a new user runs, failing on No such file.
        #     1  11-feature-request-author-...-sequence.svg — the reference in
        #        appendices.md was rewritten to 11-task-author-..., the file was not.
        #        Broke `docs-site build` on every commit ("MISSING ASSET").
        #
        # Same shape as the vocabulary gate renaming its own caller: a codemod
        # rewrites text, so it moves references and leaves targets. `git mv` is a
        # different operation from `re.sub`, and only PATH_RENAMES did it — for the
        # directories someone remembered to list.
        #
        # `(?<!N)` keeps NFR- (non-functional requirements): ~470 legitimate files.
        name_pat = re.compile(r"(?<!N)FR-|feature[-_]request|(?<![A-Za-z0-9])fr[-_]")
        bad_names = [f for f in files
                     if name_pat.search(Path(f).name)
                     and not any(x in f for x in RESIDUE_EXEMPT)]
        print(f"\n--- filename residue ---")
        if not bad_names:
            print("none")
        else:
            print(f"{'old vocabulary in FILENAME':32} {len(bad_names):7}  files")
            for f in sorted(bad_names)[:10]:
                print(f"{'':34}   {f}")
            if len(bad_names) > 10:
                print(f"{'':34}   ... +{len(bad_names) - 10} more")
            residue["filename"] = len(bad_names)

    if args.apply:
        print("\n--- path renames (git mv) ---")
        for src, dst in PATH_RENAMES:
            if freed and (src, dst) in ONE_SHOT_PATH_RENAMES:
                print(f"skip (ONE-SHOT, already ran)  {src} -> {dst}")
                continue
            s, d = root / src, root / dst
            if not s.exists():
                print(f"skip (absent)  {src}")
                continue
            if d.exists():
                # git mv into an existing directory NESTS instead of failing.
                # That is how contracts/task ended up inside contracts/subtask/task/.
                print(f"REFUSING       {src} -> {dst}  (destination exists; would nest)")
                continue
            d.parent.mkdir(parents=True, exist_ok=True)
            subprocess.run(["git", "mv", src, dst], cwd=root, check=True)
            print(f"moved          {src} -> {dst}")

        # ── The 563 per-spec directories: docs/tasks/<mod>/FR-MOD-NNN-slug/ ──
        print("\n--- spec directory renames (FR-* -> TASK-*) ---")
        tasks_root = root / "docs" / "tasks"
        moved = 0
        if tasks_root.is_dir():
            # Deepest-first so a parent rename cannot invalidate a child path.
            cands = sorted(
                (p for p in tasks_root.rglob("*") if p.is_dir() and p.name.startswith("FR-")),
                key=lambda p: len(p.parts), reverse=True,
            )
            for p in cands:
                new_name = re.sub(r"^FR-", "TASK-", p.name)
                subprocess.run(
                    ["git", "mv", str(p.relative_to(root)),
                     str((p.parent / new_name).relative_to(root))],
                    cwd=root, check=True,
                )
                moved += 1
        print(f"renamed {moved} spec directories")

        fix_symlinks(root)

        print("\n--- next, by hand ---")
        print("  1. the 13 agent-config files (.cursorrules, .windsurfrules, .grok/GROK.md,")
        print("     .github/copilot-instructions.md, .agents/rules/, .cursor/rules/, AGENTS.md,")
        print("     CLAUDE.md, GEMINI.md, .gitignore managed block, .githooks/pre-commit)")
        print("  2. the bare-FR prose pass (see --residue; 2-char token, needs review)")
        print("  3. .git-blame-ignore-revs <- sha of this commit")
        print("  4. --emit-brain-ops | python3 -m cyberos.writer apply   (never sed the store)")

    if args.verify and residue:
        print("\nVERIFY FAILED: residue remains.")
        return 1
    return 0


def emit_brain_ops(root: Path) -> int:
    """The BRAIN store cannot be sed'd. Emit protocol-legal move+put ops instead.

    Every memory file body's SHA-256 is recorded in an audit row (AGENTS.md §5.3),
    and the ledger is a hash chain (§6.3). Editing a body in place makes the
    recorded content_sha256 wrong and flips the store to FROZEN_RECOVERABLE.
    The protocol-legal expression of a rename is a new move() followed by a new
    put() — the chain then records the rename rather than being invalidated by it.
    """
    store = root / ".cyberos" / "memory" / "store"
    if not store.is_dir():
        print("no BRAIN store at .cyberos/memory/store", file=sys.stderr)
        return 2

    fr_id = re.compile(r"(?i)\bfr-([a-z]{2,10})-(\d{3,4})\b")
    ops = 0
    for p in sorted(store.rglob("*.md")):
        rel = p.relative_to(store).as_posix()
        if rel.startswith(("audit/", "exports/", "index/")):
            continue
        body = p.read_text(encoding="utf-8", errors="replace")
        new_rel = fr_id.sub(lambda m: f"TASK-{m.group(1).upper()}-{m.group(2)}"
                            if m.group(0)[0].isupper() else f"task-{m.group(1)}-{m.group(2)}", rel)
        new_body, _ = rewrite(body, rel)
        if new_rel != rel:
            print(json.dumps({"op": "move", "src": rel, "dst": new_rel}))
            ops += 1
        if new_body != body:
            print(json.dumps({
                "op": "put", "path": new_rel, "body": new_body,
                "extra": {"reason": "fr-to-task-rename-epoch"},
            }))
            ops += 1
    print(f"# {ops} ops — pipe into: python3 -m cyberos.writer apply --ndjson -",
          file=sys.stderr)
    return 0


