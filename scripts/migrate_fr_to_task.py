# ─────────────────────────────────────────────────────────────────────────────
# Content rules, applied IN ORDER. Longest / most-specific identifiers first so
# a short pattern cannot eat a long one.
#
# Ordering note: pass 1 frees the word `task` by moving the existing task@1
# contract to subtask@1. Only after that does pass 5 introduce `task@1` as the
# new name for feature_request@1. Running these out of order silently collapses
# the two contracts into one.
#
# Word-boundary note: \btask\b does NOT match inside "subtask" or "sub_tasks"
# (b/t and _/t are both word characters, so there is no boundary). That is what
# makes the subtask-first ordering safe.
# ─────────────────────────────────────────────────────────────────────────────
RULES: list[tuple[str, str, str]] = [
    # ── Pass 1: free the word. The existing task@1 becomes subtask@1. ────────
    ("free-word:contract-literal",   r"\btask@1\b",                    "subtask@1"),
    ("free-word:contract-id",        r"^contract_id: task$",           "contract_id: subtask"),
    ("free-word:contract-path",      r"contracts/task\b",              "contracts/subtask"),
    ("free-word:runner-module",      r"\bfr_with_tasks\b",             "task_with_subtasks"),
    ("free-word:runner-skill-id",    r"\bfr-with-tasks\b",             "task-with-subtasks"),
    ("free-word:runner-class",       r"\bFrWithTasksRunner\b",         "TaskWithSubtasksRunner"),
    ("free-word:frontmatter-field",  r"\bsub_tasks\b",                 "subtasks"),
    # subtask id sub-format: FR-007-T-03 -> TASK-007-S-03 (the -T- infix would
    # read as "task" inside a TASK- id; -S- is unambiguous).
    ("free-word:subtask-id",         r"\bFR-(\d+)-T-(\d{2})\b",        r"TASK-\1-S-\2"),

    # ── Pass 2: FR IDs. D2 = full rewrite. ──────────────────────────────────
    ("id:fr-module-num",             r"\bFR-([A-Z]{2,10})-(\d{3,4})\b", r"TASK-\1-\2"),
    # lowercase form appears in generated filenames (impl-plan-fr-mcp-003.md)
    ("id:fr-module-num-lower",       r"\bfr-([a-z]{2,10})-(\d{3,4})\b", r"task-\1-\2"),

    # ── Pass 2b: the MATCHERS. Most dangerous class in the whole rename. ────
    #
    # Renaming the IDs is not enough. ~40 regexes and globs across 20 files
    # match ON the id format, and pass 1 left every one of them saying `FR-`:
    #
    #     glob.glob(f"{root}/**/FR-*.md")          -> 0 files
    #     re.compile(r"FR-[A-Z]+-\d+")             -> 0 rows
    #     if not id.startswith("FR-")              -> skips everything
    #     /\bFR-[A-Z][A-Z0-9]*-\d+\b/g             -> 0 matches
    #     find ... -name 'FR-*.md'                 -> 0 results
    #
    # None of these THROW. They return empty. The backlog reader finds no rows,
    # the generators emit empty artefacts, and `ship-tasks` reports "no eligible
    # task" forever. A silent, total functional failure that a syntax check, a
    # type check and a smoke test all pass cleanly.
    #
    # I tried to enumerate the matcher SHAPES and lost. Each fix revealed another:
    #   FR-[A-Z]+-\d+     FR-\d+-T-\d{2}    FR-*.md        "FR-"      'FR-'
    #   FR-([A-Z]+)-\d+   f"FR-{id}-*.md"   /^FR-/         /^FR-.*\.md$/
    # After the id rewrite there is NO legitimate remaining `FR-` in source: every
    # id is TASK- now, so anything still saying FR- is either a matcher or prose,
    # and both must move. Stop enumerating shapes; rename the prefix.
    #
    # `\b` is the only thing standing between this rule and `NFR-` -> `NTASK-`,
    # which would silently destroy the non-functional-requirements pipeline.
    ("re:fr-prefix-blanket",         r"\bFR-",                         "TASK-"),
    # subtask id format changed too: FR-NNN-T-MM -> TASK-NNN-S-MM (see §7.1)
    # NOTE the doubled backslashes in the REPLACEMENT. `\d` is not a valid escape in
    # an re.sub replacement string — Python raises `re.error: bad escape \d`. The
    # pattern side wants `\\d` (a literal backslash-d in the file); the replacement
    # side wants `\\\\d` in source, i.e. `\\d` in the raw string.
    ("re:subtask-id-pattern",        r"\^TASK-\\d\+-T-\\d\{2\}\$",     r"^TASK-\\d+-S-\\d{2}$"),

    # ── Pass 3: artefact + skill + command identifiers. ──────────────────────
    ("artefact:feature-request-1",   r"\bfeature_request@1\b",         "task@1"),
    ("artefact:fr-manifest",         r"\bfr-manifest@2\b",             "task-manifest@1"),
    ("skill:fr-author",              r"\bfeature-request-author\b",    "task-author"),
    ("skill:fr-audit",               r"\bfeature-request-audit\b",     "task-audit"),
    ("cmd:ship",                     r"\bship-feature-requests\b",     "ship-tasks"),
    ("cmd:create",                   r"\bcreate-feature-requests\b",   "create-tasks"),

    # ── Pass 4: paths. Must run AFTER the skill/command rules above, because
    #     "docs/feature-requests/" and "contracts/feature-request/" share a stem
    #     with the skill names. ─────────────────────────────────────────────────
    ("path:docs-dir",                r"docs/feature-requests\b",       "docs/tasks"),
    ("path:contract-dir",            r"contracts/feature-request\b",   "contracts/task"),

    # ── Pass 5: frontmatter fields carrying FR IDs. ──────────────────────────
    ("field:related-frs",            r"\brelated_frs\b",               "related_tasks"),

    # ── Pass 5a: FILENAME references.
    #
    # PASS-3 ADDITIONS. `git mv` renames a file; it does NOT rename the places
    # that CALL it by name. Pass 1 moved render-fr-pages.mjs -> render-task-pages.mjs
    # while tools/docs-site/build.sh went on invoking `node render-fr-pages.mjs`,
    # which is what broke the docs-site build. Same for every other moved script.
    #
    # Rule of thumb for any future rename: for each entry in PATH_RENAMES there
    # MUST be a matching content rule for its basename, or the move is a landmine.
    ("file:render-fr-catalog",       r"render-fr-catalog",             "render-task-catalog"),
    ("file:render-fr-pages",         r"render-fr-pages",               "render-task-pages"),
    ("file:test-render-fr-pages",    r"test_render_fr_pages",          "test_render_task_pages"),
    ("file:migrate-improvement",     r"migrate_improvement_to_fr",     "migrate_improvement_to_task"),
    ("file:migrate-fr-layout",       r"migrate_fr_layout",             "migrate_task_layout"),
    ("file:test-fr-layout",          r"test_fr_layout",                "test_task_layout"),
    ("file:repair-fr-yaml",          r"repair_fr_yaml",                "repair_task_yaml"),
    ("file:rebaseline-fr-status",    r"rebaseline_fr_status",          "rebaseline_task_status"),
    ("file:awh-goldenset-from-fr",   r"awh_goldenset_from_fr",         "awh_goldenset_from_task"),
    ("file:fr-migrate-sh",           r"\bfr-migrate\b",                "task-migrate"),
    ("file:ship-your-first-fr",      r"ship-your-first-fr\b",          "ship-your-first-task"),

    # ── Pass 5a2: `fr` as a snake_case / kebab TOKEN. ───────────────────────
    #
    # Pass 1 enumerated six identifiers (fr_id, fr_row, FrRow, _FR_ROW_RE, _FR_ID_RE,
    # frs) and missed 37 more across 357 sites: fr_path, fr_root, fr_dir, fr_file,
    # ship_fr, fr_routed_back, fr_list_sorted, next_fr, dep_fr_id, _cyberos_fr_migrate,
    # CYBEROS_FR_BASE, ...
    #
    # Enumerating names does not converge — every pass found more. Rename the TOKEN.
    # `\b` is useless here: `_` is a word character, so `\bfr_` never matches inside
    # `_cyberos_fr_migrate`. That is precisely how it survived four passes.
    #
    # Protected by FROZEN_LITERALS: this script's own name (see the note there).
    ("token:fr_-lead",               r"(?<![A-Za-z0-9])fr_",           "task_"),
    ("token:_fr-tail",               r"_fr(?![A-Za-z0-9_])",           "_task"),
    ("token:FR_-const",              r"(?<![A-Za-z0-9])FR_",           "TASK_"),
    ("token:fr-kebab",               r"(?<![A-Za-z0-9_])fr-(?=[a-z])", "task-"),

    # ── Pass 5b: code identifiers. D5 = task_id, module-scoped.
    #     Safe at the rename site: `fr_id` appears only in modules/cuo,
    #     modules/skill, scripts/ and tools/ — never in services/mcp-gateway
    #     (which owns mcp_tasks.task_id) or tools/awh (eval task_id). The
    #     overload is repo-wide and semantic, not a shadowing bug. It is
    #     recorded in an ADR and enforced by check_sep986_naming.sh. ─────────
    ("ident:fr-id",                  r"\bfr_id\b",                     "task_id"),
    ("ident:fr-row",                 r"\bfr_row\b",                    "task_row"),
    ("ident:FrRow",                  r"\bFrRow\b",                     "TaskRow"),
    ("ident:FR_ROW_RE",              r"\b_FR_ROW_RE\b",                "_TASK_ROW_RE"),
    ("ident:FR_ID_RE",               r"\b_FR_ID_RE\b",                 "_TASK_ID_RE"),
    # WAS: r"\bfrs\b(?=\s*[:=\)\],])" — scoped to DEFINITION sites only.
    # That renamed `def select_next(frs: list)` -> `(tasks: list)` and
    # `frs = {}` -> `tasks = {}` while leaving every USE site (`for f in frs`,
    # `frs[k]`, `dep in frs`) pointing at a name that no longer exists.
    # Result: NameError at runtime in ship_manifest.select_next(), plus two
    # project-cleanup generators silently half-renamed. Syntax stayed valid,
    # so nothing caught it until the test suite ran.
    #
    # An identifier rename is all-or-nothing. Rename the whole symbol.
    # `(?<![."'])...(?![."'])` keeps this off the CSS CLASS `frs`, which is a
    # different thing wearing the same three letters:
    #     status-app.js   '<div class="frs">'        <- markup, must not change
    #     status.css      .frs { display: flex; }    <- and `.tasks` ALREADY exists
    #                                                   there meaning something else,
    #                                                   so renaming it would collide
    # The Python identifier this rule is for is never quoted or dot-prefixed.
    ("ident:frs-var",                r"(?<![.\"'])\bfrs\b(?![.\"'])",  "tasks"),

    # ── Pass 6: remaining identifiers, then prose. ───────────────────────────
    #
    # PASS-2 ADDITIONS. The first run left ~90 sites behind because `_` is a word
    # character, so `\bfeature_request\b` never matched INSIDE a longer snake_case
    # identifier:
    #     _apply_feature_request_audit      (cuo/core/applier.py — function name)
    #     feature_request_audit:            (backlog-state-update envelope FIELD —
    #                                        required by the BSU rubric for the
    #                                        draft -> ready_to_implement transition)
    #     feature_request_author_validates  (skill-broker test fn names)
    # Handle the compounds explicitly, longest first, then drop the boundary.
    ("ident:apply-fr-audit-fn",      r"_apply_feature_request_audit",  "_apply_task_audit"),
    ("ident:fr-audit-field",         r"feature_request_audit",         "task_audit"),
    ("ident:fr-author-field",        r"feature_request_author",        "task_author"),
    ("ident:feature-request-snake",  r"feature_request",               "task"),

    ("ident:feature-requests-kebab", r"\bfeature-requests\b",          "tasks"),
    ("ident:feature-request-kebab",  r"\bfeature-request\b",           "task"),

    # Prose. The first run missed hyphenated Title Case ("Phase F - Feature-Request
    # Authoring") and sentence case ("Feature request — engineering-spec@1", the
    # user-visible kind label on every rendered docs page).
    ("prose:Feature-Requests-hyph",  r"\bFeature-Requests\b",          "Tasks"),
    ("prose:Feature-Request-hyph",   r"\bFeature-Request\b",           "Task"),
    ("prose:Feature-Requests",       r"\bFeature Requests\b",          "Tasks"),
    ("prose:Feature-requests-sent",  r"\bFeature requests\b",          "Tasks"),
    ("prose:Feature-Request",        r"\bFeature Request\b",           "Task"),
    ("prose:Feature-request-sent",   r"\bFeature request\b",           "Task"),
    ("prose:feature-requests",       r"\bfeature requests\b",          "tasks"),
    ("prose:feature-request",        r"\bfeature request\b",           "task"),
]

# ─────────────────────────────────────────────────────────────────────────────
# PROSE-ONLY rules. These MUST NOT run on source code.
#
# The bare abbreviation `FR` is prose, and rewriting it to `task` produces
# lowercase text mid-sentence, so it needs a capitalisation repair afterwards.
# That repair is where it bit: the rule
#
#     ^(\s*(?:[-*>#]+\s+|\d+\.\s+)?)task\b   ->   \1Task
#
# is correct for a markdown bullet or heading, and catastrophic in Python, where
# it rewrote a dataclass field
#
#     task: str      ->      Task: str        (episode.py, 21 tests red)
#
# and every keyword argument at line start. `TaskRow` / `TaskCreate` survived only
# because \b needs a non-word char after "Task".
#
# Lesson: a prose rule applied to code is not a typo, it is a different program.
# Gate every prose rule on the file extension.
# ─────────────────────────────────────────────────────────────────────────────
PROSE_ONLY_SUFFIXES = (".md", ".txt", ".html", ".mdc", ".rst")

# ─────────────────────────────────────────────────────────────────────────────
# The MIRROR of the prose problem, and the third time this lesson showed up.
#
# `ident:frs-var` renames the Python identifier `frs` -> `tasks`. Correct in code.
# In prose it fires on things that are not identifiers at all:
#
#     "manifest_path": "./frs/manifest.json"      an envelope example path
#     via the existing `migrate-frs.sh --page`    a historical filename
#     documented in `implement-backlog-frs`       a retired skill name
#     website/build/data/{nfrs,risks,frs}.json    a data filename
#
# Rewriting those produces nonsense and makes `--verify` fail forever on content
# that is correct. A gate that can never pass is a gate people learn to ignore —
# which is the exact failure mode this session kept finding elsewhere (`declare -A`
# silently passing on bash 3.2, 29 trigger tests green against a monkeypatched fake).
#
# So: identifier rules run ONLY on code, prose rules run ONLY on prose, and the ones
# that are safe everywhere (ids, paths, artefact names) run on both.
# ─────────────────────────────────────────────────────────────────────────────
CODE_ONLY_RULES = frozenset({
    "ident:frs-var",
    "ident:fr-id", "ident:fr-row", "ident:FrRow",
    "ident:FR_ROW_RE", "ident:FR_ID_RE",
    "ident:apply-fr-audit-fn",
})
CODE_SUFFIXES = (".py", ".rs", ".mjs", ".js", ".ts", ".tsx", ".sh", ".yaml", ".yml",
                 ".json", ".toml")

PROSE_RULES: list[tuple[str, str, str]] = [
    ("prose:FRs",                    r"\bFRs\b",                       "tasks"),
    ("prose:FR",                     r"\bFR\b",                        "task"),
]

# Applied only after PROSE_RULES, only to PROSE_ONLY_SUFFIXES.
PROSE_FIXUPS: list[tuple[str, str]] = [
    (r"(?m)^(\s*(?:[-*>#]+\s+|\d+\.\s+)?)task\b", r"\1Task"),  # bullet / heading start
    (r"([.!?]\s+)task\b",                         r"\1Task"),  # sentence start
    (r"(\|\s*)task(\s*\|)",                       r"\1Task\2"),  # lone table cell
    # "an FR" -> "an task". FR is read "eff-arr" (vowel sound); task is not.
    (r"\bAn task\b", "A task"), (r"\ban task\b", "a task"),
    (r"\bAn Task\b", "A Task"), (r"\ban Task\b", "a Task"),
]

# The bare `FR` abbreviation is NOT in RULES. It is 2 characters and appears
# inside unrelated words, inside the FR-<MOD>-<NNN> ids handled above, and in
# third-party prose. --residue reports every remaining site for a human pass.
# ─────────────────────────────────────────────────────────────────────────────
# RULE-INDEPENDENT completeness scan.
#
# --verify used to re-run the RULES and report what THEY still matched. That is a
# coverage check wearing a completeness check's clothes: a missing rule is
# indistinguishable from a clean tree. It reported "0 residue" while 357 `fr_*`
# identifiers were still in the tree across 37 names the rules had never heard of.
#
# Same shape as every other false-green this migration turned up: bash 3.2
# swallowing `declare -A`, 29 trigger tests passing against a monkeypatched
# `classify`, a parser returning 0 rows because its regex matched nothing.
#
# These patterns know nothing about the rules. They ask the only question that
# matters: is the retired vocabulary still here?
# ─────────────────────────────────────────────────────────────────────────────
# Rule-INDEPENDENT. These answer "is the retired vocabulary still here?", never "did my
# rules fire?" — see §14.6. Three gaps found on 2026-07-15, each of which let a live bug
# through a --verify that reported `none`:
#
#   1. `FR-id-prefix` was `\bFR-[A-Z0-9]`. Source text `/\bFR-[A-Z]...` (a JS regex
#      literal) has the letter `b` immediately before `FR` — the tail of the escape
#      `\b`. `b` is a word char, so `\b` asserts no boundary and the pattern MISSED.
#      A word-boundary check defeated by the letter inside `\b`. It also required
#      [A-Z0-9] after the dash, which `FR-[A-Z]` (literal bracket) fails.
#      -> Now a bare literal. Any `FR-` at all is residue.
#      Cost of the miss: render-status-hub.mjs kept `const TASKID = /\bFR-.../`,
#      `cited` came back [] for every release, and the changelog -> task binding was
#      dead on the live status page.
#
#   2. Bare lowercase `fr` as a path segment or word (`docs/status/data/fr/`) matched
#      nothing: `bare-FR-abbrev` is uppercase-only, `fr-kebab` needs a trailing dash,
#      `fr_-identifier` needs an underscore. Cost: audit-fleet.sh counted 0 chunks
#      against 509 specs and flagged every healthy repo `no-spec-chunks`.
#
#   3. `-fr` as a token TAIL (`data-fr="..."`) matched nothing for the same reason —
#      every rule keys on `fr-` or `fr_`, never `-fr`.
#
# The shape of all three: a pattern written to describe the rules, not the vocabulary.
RESIDUE_PATTERNS = [
    ("bare-FR-abbrev",   re.compile(r"(?<![A-Za-z0-9_-])FRs?(?![A-Za-z0-9_-])")),
    # `(?<!N)` because NFR- (non-functional requirement) legitimately contains FR- and is
    # NOT part of this rename — 498 live ids under docs/non-functional-requirements/.
    # Not \b: the JS source `/\bFR-.../` has the letter `b` before FR, which kills the
    # boundary (that is exactly what hid the dead TASKID regex).
    ("FR-id-prefix",     re.compile(r"(?<!N)FR-")),
    ("feature-request",  re.compile(r"(?i)feature[-_ ]request")),
    ("fr_-identifier",   re.compile(r"(?<![A-Za-z0-9])fr_|_fr(?![A-Za-z0-9_])|(?<![A-Za-z0-9])FR_")),
    ("fr-kebab",         re.compile(r"(?<![A-Za-z0-9_])fr-(?=[a-z])")),
    ("fr-bare-token",    re.compile(r"(?<![A-Za-z0-9_-])fr(?![A-Za-z0-9_-])")),
    ("fr-token-tail",    re.compile(r"[-/]fr(?![A-Za-z0-9_])")),
]

# Literals that MUST survive the rename, on any line, comment or not. Each is a
# reference whose SUBJECT is the retired vocabulary — the same reasoning that
# deny-lists RENAME-EPOCH.md, and the reason --verify can reach exit 0 at all:
#
#   migrate_fr_to_task.py       this script's own filename (a proper noun)
#   fr-memory-109               FROZEN provenance value, written into memory-file
#                               frontmatter; the BRAIN still holds it (FROZEN_LITERALS)
