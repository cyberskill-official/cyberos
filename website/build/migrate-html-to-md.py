#!/usr/bin/env python3
"""One-time FR-DOCS-002 migration: hand-authored website HTML -> markdown sources.

Extracts each page's <main> content and converts it to markdown at the owning
scope (module code home, or global docs/). Idempotent: skips targets that exist.
Requires html2text (pip install html2text). Delete after the migration lands.
"""
from pathlib import Path
import re
import sys

import html2text

ROOT = Path(__file__).resolve().parents[2]
SITE = ROOT / "website" / "docs"

# module -> docs home (relative to repo root). Modules without a code home yet
# live under global docs/modules/<m>/ and move next to their code when it lands.
MODULE_HOMES = {
    "memory": "modules/memory/docs",
    "cuo": "modules/cuo/docs",
    "skill": "modules/skill/docs",
    "ai": "services/ai-gateway/docs",
    "auth": "services/auth/docs",
    "chat": "services/chat/docs",
    "email": "services/email/docs",
    "proj": "services/proj/docs",
    "mcp": "services/mcp-gateway/docs",
    "obs": "services/obs-collector/docs",
}

def module_home(mod: str) -> Path:
    rel = MODULE_HOMES.get(mod, f"docs/modules/{mod}")
    return ROOT / rel

def convert(src: Path, dst: Path) -> str:
    html = src.read_text(encoding="utf-8")
    m = re.search(r"<main\b[^>]*>(.*)</main>", html, re.S | re.I)
    body = m.group(1) if m else html
    title = ""
    t = re.search(r"<title>(.*?)</title>", html, re.S | re.I)
    if t:
        title = re.sub(r"\s+", " ", t.group(1)).strip()
    h = html2text.HTML2Text()
    h.body_width = 0            # no hard wrapping - diffs stay sane
    h.ignore_images = False
    h.protect_links = True
    h.mark_code = False
    md = h.handle(body).strip() + "\n"
    fm = f"---\ntitle: {title or dst.stem}\nsource: website/docs/{src.relative_to(SITE)}\nmigrated: FR-DOCS-002\n---\n\n"
    if dst.exists():
        return "skip (exists)"
    dst.parent.mkdir(parents=True, exist_ok=True)
    dst.write_text(fm + md, encoding="utf-8")
    return "ok"

def main() -> int:
    jobs: list[tuple[Path, Path]] = []
    for page in sorted((SITE / "architecture").glob("*.html")):
        jobs.append((page, ROOT / "docs" / "architecture" / (page.stem + ".md")))
    for name in ("getting-started", "glossary", "risk-register"):
        p = SITE / "reference" / f"{name}.html"
        if p.exists():
            jobs.append((p, ROOT / "docs" / "reference" / f"{name}.md"))
    for mdir in sorted((SITE / "modules").glob("*/")):
        mod = mdir.name
        home = module_home(mod)
        for page in sorted(mdir.glob("*.html")):
            if page.stem == "changelog":
                continue  # generated by render-module-changelog.mjs
            name = "index" if page.stem == "index" else page.stem
            jobs.append((page, home / f"{name}.md"))
    ok = 0
    for src, dst in jobs:
        res = convert(src, dst)
        print(f"{res:14} {src.relative_to(SITE)} -> {dst.relative_to(ROOT)}")
        ok += res == "ok"
    print(f"migrated {ok}/{len(jobs)}")
    return 0

if __name__ == "__main__":
    sys.exit(main())
